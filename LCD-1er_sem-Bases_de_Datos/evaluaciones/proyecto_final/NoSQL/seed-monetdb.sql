CREATE TABLE Sensor(
    IdS INT NOT NULL,
    Longitud FLOAT NOT NULL,
    latitud FLOAT NOT NULL,
    PRIMARY KEY(IDS)
);
CREATE TABLE Lectura(
    IdL INT NOT NULL,
    Fecha TIMESTAMP NOT NULL,
    IdS INT NOT NULL,
    FOREIGN KEY (IdS) REFERENCES Sensor(IdS) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY KEY(IdL)
);
CREATE TABLE Lectura_Temperatura(
    IdL INT NOT NULL,
    SURFACE_TEMPERATURE FLOAT NOT NULL,
    SOIL_TEMPERATURE FLOAT NOT NULL,
    FOREIGN KEY (IdL) REFERENCES Lectura(IdL) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY KEY(IdL)
);
CREATE TABLE Lectura_Aire(
    IdL INT NOT NULL,
    AIR_TEMPERATURE FLOAT NOT NULL,
    WIND FLOAT NOT NULL,
    FOREIGN KEY (IdL) REFERENCES Lectura(IdL) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY KEY(IdL)
);
CREATE TABLE Lectura_Precipitaciones(
    IdL INT NOT NULL,
    precipitation FLOAT NOT NULL,
    WETNESS FLOAT NOT NULL,
    FOREIGN KEY (IdL) REFERENCES Lectura(IdL) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY KEY(IdL)
);
CREATE TABLE Lectura_RadiacionSolar(
    IdL INT NOT NULL,
    SOLAR_RADIATION FLOAT NOT NULL,
    FOREIGN KEY (IdL) REFERENCES Lectura(IdL) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY Key(IdL)
);
CREATE TABLE Lectura_Humedad(
    IdL INT NOT NULL,
    RELATIVE_HUMIDITY FLOAT NOT NULL,
    SOIL_MOISTURE FLOAT NOT NULL,
    FOREIGN KEY (IdL) REFERENCES Lectura(IdL) ON DELETE RESTRICT ON UPDATE CASCADE,
    PRIMARY KEY(IdL)
);


